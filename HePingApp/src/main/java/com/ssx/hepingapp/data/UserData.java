package com.ssx.hepingapp.data;

public class UserData {
    private int id;
    private String touxiang;
    private String name;
    private int sex;
    private int zhuangtai;
    private long time;
    private int quyu;
    private int zhiwu;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTouxiang() {
        return touxiang;
    }

    public void setTouxiang(String touxiang) {
        this.touxiang = touxiang;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSex() {
        return sex;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }

    public int getZhuangtai() {
        return zhuangtai;
    }

    public void setZhuangtai(int zhuangtai) {
        this.zhuangtai = zhuangtai;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public int getQuyu() {
        return quyu;
    }

    public void setQuyu(int quyu) {
        this.quyu = quyu;
    }

    public int getZhiwu() {
        return zhiwu;
    }

    public void setZhiwu(int zhiwu) {
        this.zhiwu = zhiwu;
    }
}
